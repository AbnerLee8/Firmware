//Convert a single RGB data into SPI data
void user_rgb_set_only(SPI_COLOUR *spi_buff,RGB_COLOUR *rgb,RGB_CODE *code)
{
	//printf("user_rgb_set_only\n");

    if(!spi_buff || !rgb){
        return;
    }

    for(int i=0;i<4;i++){
        if(rgb->r&BIT(7-i)){
            spi_buff->r[i]=code->code_1;
			spi_buff->r[i]<<=4;
        }else{
            spi_buff->r[i]=code->code_0;
			spi_buff->r[i]<<=4;
        }
        if(rgb->g&BIT(7-i)){
            spi_buff->g[i]=code->code_1;
			spi_buff->g[i]<<=4;
        }else{
            spi_buff->g[i]=code->code_0;
			spi_buff->g[i]<<=4;
        }
        if(rgb->b&BIT(7-i)){
            spi_buff->b[i]=code->code_1;
			spi_buff->b[i]<<=4;
        }else{
            spi_buff->b[i]=code->code_0;
			spi_buff->b[i]<<=4;
        }
		
		if(rgb->r&BIT(3-i)){
            spi_buff->r[i]|=code->code_1;
        }else{
            spi_buff->r[i]|=code->code_0;
        }
        if(rgb->g&BIT(3-i)){
            spi_buff->g[i]|=code->code_1;
        }else{
            spi_buff->g[i]|=code->code_0;
        }
        if(rgb->b&BIT(3-i)){
            spi_buff->b[i]|=code->code_1;
        }else{
            spi_buff->b[i]|=code->code_0;
        }
    }
}

//Convert all color data into SPI data
void user_rgb_set_all(void *priv)
{
	//printf("user_rgb_set_all\n");

    RGB_INFO *rgb = (RGB_INFO *)priv;

    if(!rgb || !rgb->spi_buff || !rgb->rgb_buff || !rgb->number){
        return;
    }
    
    SPI_COLOUR *P=rgb->spi_buff;


    rgb->rend_flag = 1;
    for(int i=0;i<rgb->number;i++){
        user_rgb_set_only(P+i,rgb->rgb_buff+i,&(rgb->code));
    }
    rgb->rend_flag = 0;

    return;
}

//Set the RGB color for the which number lights
/*
Priv: RGB configuration data
Color: RGB color
number: The light closest to the IO port is the first one, and the last one can be represented as -1 or (USER_RGBNUMBER-1)
*/

void user_rgb_colour_only_set(void *priv,RGB_COLOUR *colour,signed short number)
{
	//printf("user_rgb_colour_only_set number is :number=%d \n",number);

    RGB_INFO *rgb = (RGB_INFO *)priv;
    signed short mapp_number = number;

    if(!rgb || !colour){
        return;
    }

    mapp_number%=rgb->number;

    mapp_number = mapp_number<0?(rgb->number+mapp_number):mapp_number;

    rgb->rgb_buff[mapp_number]=*colour;
//    for( int i=0;i<mapp_number;i++)
//    printf("map[%d] 0x%02x \n",mapp_number,rgb->rgb_buff[mapp_number]);
    return;
}

//Set to the same color
void user_rgb_same_colour(void *priv,RGB_COLOUR *colour)
{
   //printf("user_rgb_same_colour\n");
    RGB_INFO *rgb = (RGB_INFO *)priv;
    if(!rgb || !colour || !rgb->number){
        return;
    }

    for(int i=0;i<rgb->number;i++){
        user_rgb_colour_only_set(rgb,colour,i);
    }
}
//clear the color
void user_rgb_clear_colour(void *priv)
{
	//printf("user_rgb_clear_colour\n");

    RGB_INFO *rgb = (RGB_INFO *)priv;
    RGB_COLOUR colour={0,0,0};

    if(!rgb || !rgb->number){
        return;
    }

    user_rgb_same_colour(rgb,&colour);
}
