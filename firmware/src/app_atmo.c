void user_rgb_send_ctrl(unsigned char cmd)
{
	//printf("user_rgb_send_ctrl\n");

    if(!_this_rgb){
        return;
    }

    if(0 == cmd){
        _this_rgb->updata_only = 0;
    }else if(0xff == cmd){
        _this_rgb->updata_only = 0xff;
    }else if(cmd){
        _this_rgb->updata_only = cmd;
    }

    if(_this_rgb->updata_only){
        user_rgb_spi_send_once();
    }
}

